    /** @var list<CountryCode>|null */
    private ?array $list = null;

    private function __construct()
    {
    }

    public static function create(): self
    {
        return new self();
    }

    /** @return list<CountryCode> */
    private function hydrateList(): array
    {
        if ($this->list === null) {
            $this->list = array_map(
                static function (string $code): CountryCode {
                    assert($code !== '');

                    return CountryCode::fromString($code);
                },
                self::LIST
            );
        }

        return $this->list;
    }

    /** @return Traversable<array-key, CountryCode> */
    public function getIterator(): Traversable
    {
        return new ArrayIterator($this->hydrateList());
    }

    public function count(): int
    {
        return count(self::LIST);
    }

    /** @return list<CountryCode> */
    public function toArray(): array
    {
        return $this->hydrateList();
    }
}
